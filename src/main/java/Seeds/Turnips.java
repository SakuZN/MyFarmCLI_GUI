package Seeds;

import main.Seeds;

/**
 * This class represents the Turnips seed (not to be confused with Turnip seed class).
 * Extends the Seeds class.
 */
public class Turnips  extends Seeds {

    /**
     * Constructor for Turnip class.
     * Calls the super constructor to set all the turnips seed properties.
     */
    public Turnips() {
        super("Flower",
                "Turnips",
                2,
                2,
                3,
                0,
                2,
                1,
                1,
                10,
                9,
                5);
    }
}

