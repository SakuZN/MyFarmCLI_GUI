package main;

/**
 * This class is used to build the GUI for the game and output a .jar file.
 * Only the GUI is built here, the game logic is in the MyFarmGUI class.
 */
public class MyFarmGUI_exec {

    /**Default constructor for MyFarmGUI_exec class.
     * Never used. */
    public MyFarmGUI_exec() {}

    /**
     * Driver method to call MyFarmGUI class which starts the game.
     * @param args the command line arguments.
     */
    public static void main(String[] args) {
        MyFarmGUI.main(args);
    }
}
